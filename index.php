<p>The Center for Life Sciences Education is an interdepartmental program
   administered by the <a href="http://artsandsciences.osu.edu/">College of
   Arts and Sciences</a> at The Ohio State University. The Center's
   mission is to develop, teach, and provide administrative support for
   the Biology major and Biology courses.</p>

<p>The introductory level majors and non-majors courses taught by the CLSE
	 are an important component of the GEC of the College of the Arts and
	 Sciences. Within the paradigm of an arts and sciences curriculum, it is
   the mission of the CLSE to provide courses that:</p>
<ul>
 <li>meet the interests and needs of the diverse population of students served,</li>
 <li>are academically challenging and rewarding,</li>
 <li>are of the highest quality in both content and presentation.</li>
</ul>

