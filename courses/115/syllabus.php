<h2>Prerequisites:</h2>
<p>Honors standing, eligibility for Math 150. Not open to students with credit for Biology 113.
   Students should have also taken high school courses in chemistry and in biology (and ideally a
   college course in general chemistry) prior to this course.</p>

<h2>Course Objectives:</h2>
<p>Students in H115-H116 will have an opportunity to:</p>
<ul>
 <li>Acquire a thorough knowledge of the fundamental terminology, concepts and processes common to
     living systems, whether plant, animal or microbe.</li>
 <li>Acquire a working knowledge of basic laboratory approaches utilized in modern biological
     investigations.</li>
 <li>Develop an understanding of the means by which a research investigator recognizes the potential
     of a given project, creates the experimental design, does the experimentation, and analyzes and
     interprets the collected data into one or more new hypotheses.</li>
 <li>Gain experience in seeking out information from reference texts, journals, reprints, and other
     sources in the Biological Sciences and Health Sciences libraries.</li>
 <li>Gain experience in problem solving, in synthesizing ideas, and in writing reports.</li>
 <li>Develop an awareness of science as a human endeavor with social consequences and
     responsibilities and with a great potential for meeting present and future challenges.</li>
</ul>
