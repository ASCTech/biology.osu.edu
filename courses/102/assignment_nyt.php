<h1><center>New York Times Weekly Assignment</center></h1>
<p><u>Objective</u>:</p>
<p>During weeks 3 through 9, students are expected to turn in, at their recitation, one
   biologically-related article from the New York Times with a short summary (no more than a single,
   typed, double-spaced, page). These articles should be from the current or immediately previous
   week. Each of the first six will be worth 7 points, with the last one being worth 8 points to
   bring the total to 50 points.</p>
<p><u>Examples of acceptable topics might include</u>:</p>
<ul>
 <li>Health care issues (Cancer, AIDS, Stem Cells, etc.)</li>
 <li>Global Warming</li>
 <li>Natural Resources</li>
 <li>Agriculture</li>
</ul>
<p><u>Examples of unacceptable topics might include</u>:</p>
<ul>
 <li>Astronomy</li>
 <li>Obituaries</li>
 <li>Book reviews</li>
</ul>
<p>Students should turn in a hard-copy of the article (cut-out, print-out or photocopy) along with
   their summary. The summary should capture the content of the article in the student's own words,
   and may reflect the student's personal thoughts on the issue.</p>
