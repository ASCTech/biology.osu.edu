<h1>Mission Statement</h1>
<p>The Mission of the Center for Life Sciences Education (CLSE) is to
	 develop, teach, and provide administrative support for the biology major
   and introductory level Biology courses at the Ohio State University. CLSE
   courses include those that satisfy the General Education Curriculum (GEC)
	 requirement for students who are not majoring in the biological sciences
	 (e.g., Biology 101, 102), introductory courses for students who are
	 planning on majoring in one of the sciences (Biology 113, 114), an Honors
	 sequence (Biology H115, H116), some of the courses that satisfy biology
	 major requirements (Biology 401, 402), and any other Biology courses that
	 might be developed and taught. The introductory level majors and
	 non-majors courses taught by the CLSE are an important component of the
	 GEC of the College of the Arts and Sciences. Within the paradigm of an
	 arts and sciences curriculum, it is the mission of the CLSE to provide
   courses that:</p>
<ul>
 <li>meet the interests and educational needs of the diverse population of students served,</li>
 <li>are academically challenging and rewarding,</li>
 <li>are of the highest quality in both content and presentation.</li>
</ul>

