<h1>Mission Statement</h1>
<p>The Mission of the Center for Life Sciences Education (CLSE) is to develop, teach, and provide
   administrative support for introductory level Biology courses taught by the College of Biological
   Sciences. These courses include those that satisfy the General Education Curriculum (GEC)
   requirement for students who are not majoring in the biological sciences (e.g., Biology 101, 102),
   courses for students who are planning on majoring in one of the biological sciences (Biology 113,
   114), an Honors sequence (Biology H115, H116), and any other Biology courses that might be
   developed and taught. The introductory level majors and non-majors courses taught by the CLSE are
   an important component of the GEC of the Colleges of the Arts and Sciences. Within the paradigm of
   an arts and sciences curriculum, it is the mission of the CLSE to provide courses that:</p>
<ul>
 <li>meet the interests and educational needs of the diverse population of students served,</li>
 <li>are academically challenging and rewarding,</li>
 <li>are of the highest quality in both content and presentation.</li>
</ul>
