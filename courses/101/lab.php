<h1>A Sample Lab</h1>
<p>One of the first labs you will experience in Biology 101 is a lab entitled &quot;How Do We Categorize Living Things?&quot; As part of this lab, you
   will take a trip to a wooded area on West Campus, where you will have the opportunity to observe and collect a variety of living things. Your
   challenge will be to collect and describe as many different species as possible, then, using your own knowledge and powers of observation, suggest
   patterns of relatedness among species and groups of species.</p>
<p>This lab also asks you to consider a variety of issues regarding diversity: What is a species? How do we tell if a group of similarly appearing (or
   not so similarly appearing) individuals belongs to the same species? How do they know they are the same species? How many species exist? What is the
   relationship between similar and dissimilar species; is there a relationship at all?</p>
<p>The abundance and diversity of species has impressed and inspired scientists, artists, poets and all major religions. Often, humans see themselves
   as separate entities, apart from &quot;nature&quot;. This lab asks you to take a closer look at the issues above, and to consider your role and the
   roles of other species, within them.</p>

