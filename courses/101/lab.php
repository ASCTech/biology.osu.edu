<h1>Biology 101 Lab Description</h1>
<p>Biology 101 lab sessions are 2 hours and 48 minutes each. This allows for approximately 1 hour of recitation, and two hours for the day's lab
   exercise. Biology 101 lab sections typically have no more than 24 students each. This gives you an opportunity to get to know your fellow students
   and teaching assistant, and to ask questions in a more intimate setting than lecture presents.</p>
<p>The laboratory exercises presented in Biology 101 have the following general form:</p>
<ul>
 <li>Cooperative. You will pose and test questions within a group of peers.</li>
 <li>Inquiry-based. Questions will be asked; you will suggest alternative, possible answers.</li>
 <li>Demonstrate the processes of science. You will be given the opportunity to evaluate your possible answers through experimentation and other forms
     of reasoning.</li>
 <li>Hands-on. The laboratory exercises presented here require you to design your own experiments and manipulate materials &ndash; that's science.</li>
 <li>Open-ended. Not all laboratory exercises you design will "work" &ndash; that's science.</li>
 <li>Non-memorization. Perhaps the greatest frustration for some students taking this course is that memorization skills honed in some earlier science
     and related courses do not serve them well here. Science is not memorization.</li>
 <li>Follow a "learning cycle" approach. This approach consists of:
  <ul>
   <li>An initial discovery phase where students working in groups address specific questions, e.g. &quot;How does the environment select for
       characteristics of organisms?&quot; or &quot;How do molecules move and out of a cell?&quot;</li>
   <li>Development and comparison of initial answers to the original question.</li>
   <li>Term introduction: appropriate vocabulary is introduced as needed to understand the processes and structures revealed.</li>
   <li>Applications. These can include:
    <ul>
     <li>Additional questions addressed in this or subsequent laboratory exercises</li>
     <li>Discussions in &quot;lecture&quot;</li>
    </ul>
   </li>
  </ul>
 </li>
</ul>
