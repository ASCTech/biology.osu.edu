<p class=MsoNormal><b><span style='font-size:11.5pt;font-family:"Times New Roman","serif"'>GOALS</span></b></p>
<p class=MsoNormal style='margin-left:27.0pt'><span style='font-size:11.5pt;
font-family:"Times New Roman","serif"'>The major goal of this course is to
provide you with a foundation of principles that are central to biology.&nbsp;
Through the study of these core concepts the nature of scientific inquiry will
be reinforced. These goals are in-line with the goals and objectives of the GEC
Natural Science category.*&nbsp; </span></p>
<p class=MsoNormal><span style='font-size:11.5pt;font-family:"Times New Roman","serif"'>&nbsp;</span></p>
<h4><span style='font-size:11.5pt;font-family:"Times New Roman","serif"'>LEARNING
OUTCOMES</span><span style='font-size:11.5pt;font-family:"Times New Roman","serif";
font-weight:normal'> </span></h4>
<p class=MsoNormal style='margin-left:27.0pt'><span style='font-size:11.5pt;
font-family:"Times New Roman","serif"'>You will achieve a sound understanding
of core concepts in biology.&nbsp; Some new terms and ideas will be introduced but
as much as possible you will not be expected to memorize facts and figures.&nbsp;
The information presented will also prepare you to make sound life choices
related to biology, e.g. Are genetically modified foods safe to eat?&nbsp; This
course is designed to provide you with the foundation of knowledge to discuss
and evaluate such topics, which will be the focus of Biology 102.&nbsp; Thus Biology
101 will provide the framework of knowledge you need for life science future
courses and biology related decisions.&nbsp; Specifically:</span></p>
<p class=MsoNormal style='margin-left:27.0pt'><span style='font-size:11.5pt;
font-family:"Times New Roman","serif"'>&nbsp;</span></p>
<ul style='font-size:11.5pt; font-family:"Times New Roman","serif"'>
<li>Students will recall current and historical aspects of energetics, genetics, evolution,
and ecology. Students will describe biological processes related to energetics,
genetics, evolution, and ecology.&nbsp; Students will analyze the current and future
significance of energetics, genetics, evolution, and ecology on society</li>
<li>Students will apply skills that demonstrate their scientific literacy by communicating
about the content and validity of articles related to science in the popular
press.</li>
<li>Students will value the study of biology.</li>
<li>Students will demonstrate an understanding of the nature of science. This includes (1)
the way that scientist develop and evaluate explanations of natural phenomena
using criteria fundamental to scientific inquiry and (2) the understanding that
science is a human endeavor.</li>
<li>Students will work productively and effectively in a group.</li>
</ul>
