<h4><span style='font-size:11.5pt;font-family:"Times New Roman","serif"'>Goals/Rationale:</span></h4>
<p class=MsoBodyTextIndent3 style='margin-left:27.0pt'><span style='font-size:
11.5pt;font-family:"Times New Roman","serif"'>Courses in natural sciences
foster an understanding of the principles, theories and methods of modern
science, the relationship between science and technology, and the effects of
science and technology on the environment.</span></p>
<p class=MsoNormal><span style='font-size:11.5pt;font-family:"Times New Roman","serif"'>&nbsp;</span></p>
<h4><span style='font-size:11.5pt;font-family:"Times New Roman","serif"'>Learning
Objectives:</span></h4>
<p class=MsoBodyTextIndent3 style='margin-left:31.5pt;text-indent:-.25in'><span
style='font-size:11.5pt;font-family:"Times New Roman","serif"'>1.<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span></span><span
style='font-size:11.5pt;font-family:"Times New Roman","serif"'>Students
understand the basic facts, principles, theories and methods of modern science.</span></p>
<p class=MsoBodyTextIndent3 style='margin-left:31.5pt;text-indent:-.25in'><span
style='font-size:11.5pt;font-family:"Times New Roman","serif"'>2.<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span></span><span
style='font-size:11.5pt;font-family:"Times New Roman","serif"'>Students learn
key events in the history of science.</span></p>
<p class=MsoBodyTextIndent3 style='margin-left:31.5pt;text-indent:-.25in'><span
style='font-size:11.5pt;font-family:"Times New Roman","serif"'>3.<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span></span><span
style='font-size:11.5pt;font-family:"Times New Roman","serif"'>Students provide
examples of the inter-dependence of scientific and technological developments.</span></p>
<p class=MsoBodyTextIndent3 style='margin-left:31.5pt;text-indent:-.25in'><span
style='font-size:11.5pt;font-family:"Times New Roman","serif"'>4.<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span></span><span
style='font-size:11.5pt;font-family:"Times New Roman","serif"'>Students discuss
social and philosophical implications of scientific discoveries and understand
the potential of science and technology to address problems of the contemporary
world.</span></p>
<p class=MsoNormal style='margin-left:.5in'><span style='font-size:11.5pt;
font-family:"Times New Roman","serif"'>&nbsp; </span></p>
<p class=MsoNormal style='margin-left:27.0pt'><span style='font-size:11.5pt;
font-family:"Times New Roman","serif"'>In Biology 101, non-major Biology
students meet the GEC Natural Science Learning Objectives in multiple ways.
Students gain an understanding of the foundations of modern biology by studying
organismal diversity, evolution, ecology, energetics, genetics, reproduction,
and cell structure and function. The students will perform inquiry-based
laboratory activities that provide insight into scientific methods and habits
of mind. Lectures and labs will include references to the development of
scientific concepts to help students understand the history and nature of
science. The interactions among science, technology, and society are interwoven
throughout the course, and assignments give students opportunities to
personally consider the interactions. Biology 101 is designed to help prepare
students to make intelligent, informed decisions on the biological and
technological decisions that they will face in life. </span></p>
<h4><span style='font-size:11.5pt;font-family:"Times New Roman","serif"'>&nbsp;</span></h4>
<h4><span style='font-size:11.5pt;font-family:"Times New Roman","serif"'>STRUCTURE
</span></h4>
<p class=MsoBodyTextIndent3 style='margin-left:27.0pt'><span style='font-size:
11.5pt;font-family:"Times New Roman","serif"'>The class will meet 3 times per
week for 1 hour and 48 minutes each session for lecture.&nbsp; The content of the
lecture is determined by the laboratory activity that week.&nbsp; Lecture time will
be used to reinforce the concepts that were introduced in lab and to expand
upon those concepts.&nbsp; A mixture of lecture and group discussion/activity
formats will be used.&nbsp; Participation by all members of the group in the
activities is expected. In addition to lecture notes you will be asked to
respond to specific questions or problems. Most of these will be group
activities, but a few will require individual responses.&nbsp; These will be
collected and graded.&nbsp; </span></p>
<p class=MsoBodyTextIndent3 style='margin-left:27.0pt'><span style='font-size:
11.5pt;font-family:"Times New Roman","serif"'>&nbsp;</span></p>
<p class=MsoBodyTextIndent3 style='margin-left:27.0pt'><span style='font-size:
11.5pt;font-family:"Times New Roman","serif"'>Laboratories meet twice weekly
for approximately 3 hrs.&nbsp; The lab activities are central to the course, as
lecture will generally be used to explain and discuss the preceding lab.&nbsp; Each
laboratory period includes a recitation period during which questions can be
answered and concepts from the lab can be reviewed.&nbsp; Review your lab manual for
lab policies and procedures.</span></p>
