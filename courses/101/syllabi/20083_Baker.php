<p>In Biology 101, non-major Biology students meet the GEC Natural Science
   Learning Objectives in multiple ways. Students gain an understanding of the
   foundations of modern biology by studying organismal diversity, evolution,
   ecology, energetics, genetics, reproduction, and cell structure and
   function. The students will perform inquiry-based laboratory activities
   that provide insight into scientific methods and habits of mind. Lectures
   and labs will include references to the development of scientific concepts
   to help students understand the history and nature of science. The
   interactions among science, technology, and society are interwoven
   throughout the course, and assignments give students opportunities to
   personally consider the interactions. Biology 101 is designed to help
   prepare students to make intelligent, informed decisions on the biological
   and technological decisions that they will face in life.</p>

<h2>Structure</h2>
<p>The class will meet 3 times per week for 1 hour and 48 minutes each session
   for lecture. The content of the lecture is determined by the laboratory
   activity that week. Lecture time will be used to reinforce the concepts
   that were introduced in lab and to expand upon those concepts. A mixture of
   lecture and group discussion/activity formats will be used. Participation
   by all members of the group in the activities is expected. In addition to
   lecture notes you will be asked to respond to specific questions or
   problems. Most of these will be group activities, but a few will require
   individual responses. These will be collected and graded.</p>
<p>Laboratories meet twice weekly for approximately 3 hrs. The lab activities
   are central to the course, as lecture will generally be used to explain and
   discuss the preceding lab. Each laboratory period includes a recitation
   period during which questions can be answered and concepts from the lab can
   be reviewed. Review your lab manual for lab policies and procedures.</p>

