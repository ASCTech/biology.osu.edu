<h2>Goals</h2>
<p>The major goal of this course is to provide you with a foundation of
   principles that are central to biology. Through the study of these core
   concepts the nature of scientific inquiry will be reinforced. These goals
   are in-line with the goals and objectives of the GEC Natural Science
   category. (outlined below)</p>

<h2>Learning Outcomes</h2>
<p>You will achieve a sound understanding of core concepts in biology. Some
   new terms and ideas will be introduced but as much as possible you will not
   be expected to memorize facts and figures. The information presented will
   also prepare you to make sound life choices related to biology, e.g. Are
   genetically modified foods safe to eat? This course is designed to provide
   you with the foundation of knowledge to discuss and evaluate such topics,
   which will be the focus of Biology 102. Thus Biology 101 will provide the
   framework of knowledge you need for life science future courses and biology
   related decisions. Specifically:</p>
<ul>
  <li>Students will recall current and historical aspects of energetics,
      genetics, evolution, and ecology. Students will describe biological
      processes related to energetics, genetics, evolution, and ecology.
      Students will analyze the current and future significance of energetics,
      genetics, evolution, and ecology on society.</li>
  <li>Students will apply skills that demonstrate their scientific literacy by
      communicating about the content and validity of articles related to
      science in the popular press.</li>
  <li>Students will value the study of biology.</li>
  <li>Students will demonstrate an understanding of the nature of science.
      This includes (1) the way that scientist develop and evaluate
      explanations of natural phenomena using criteria fundamental to
      scientific inquiry and (2) the understanding that science is a human
      endeavor.</li>
  <li>Students will work productively and effectively in a group.</li>
</ul>

