<h2>New York Times Clippings Project:</h2>
<p>A major goal of this course is to demonstrate the relevance of biology to society today. Accordingly, you should turn into your laboratory teaching
   assistant each week an article from the last week of the New York Times with a brief paragraph written explaining the article and how it indicates
   the relevancy of its biological topic to society today. These will be graded full credit or no credit and are due at the start of each lab period
   starting with the second lab exercise of the quarter. The article you submit should be clipped from the paper itself, photocopied or downloaded (the
   New York Times is available in most libraries and online at <a href="http://www.nytimes.com/">www.nytimes.com</a>).</p>

