<h2>Exams:</h2>
<p>There will be two midterm exams which should take approximately 1 hour although you will have a
   full class session, 1 hour and 18 minutes, to complete them. We expect each exam to be made up
   predominantly of multiple choice questions, but some short answer/discussion questions may
   appear. You will be responsible for material presented in lecture, recitation and in the reading
   assignments. The final exam will consist of approximately 140 points representing a
   &quot;third&quot; midterm examination from material covered after midterm #2, and 80 points which
   will be comprehensive from earlier course lecture material.</p>

<h2>Make-Up Exams:</h2>
<p>Makeup exams will be short answer/discussion. A written doctor's excuse or equivalent is required
   in order to take a make-up. If you need to take a make-up exam, it is your responsibility to
   contact us, present your valid excuse, and arrange for the make-up. It is not our responsibility
   to find you. Please bear in mind that it is a serious pain in the neck for faculty to compose and
   arrange make-up exams, especially for a class of this size. Please do not abuse the privilege.</p>

<h2>Senior Finals:</h2>
<p>Senior finals will be short answer/discussion. Graduating seniors should notify all instructional
   staff as soon as possible that you are graduating at the end of the quarter.</p>

<h2>Recitation and Quizzes:</h2>
<p>It is important to attend recitation because new material will be presented and there will be
   quizzes, which will add points to your grade. Five quizzes (15 points each) will be variable
   format, and cover both lecture and lab material. Quizzes are given as determined by your TA and
   announced in advance. A late student arriving after the class has completed a quiz will not
   receive a makeup quiz unless she/he can provide a valid written excuse as an explanation for the
   late arrival. No quiz is provided without the written excuse. If a student arrives during the
   quiz period, the student may take the quiz, but will not receive extra time for completion;
   she/he must turn in their quiz with the rest of the class.</p>

<h2>Question Diary:</h2>
<p>The question diary consists of a series of single questions presented by Dr. Fuerst toward the
   end of class time in random lecture periods throughout the quarter. Each question is of moderate
   difficulty and based on material presented in that day's lecture period. Question format may vary
   from week to week (short answer, multiple choice, etc.).</p>
<p>On the day that a question is presented, you will receive an index card at the beginning of
   lecture period for recording your answer. Only those answers recorded on the index card are
   accepted; answers on notebook or any other form of paper will not be graded. Each question is
   worth 5 points and graded on the basis of full, partial or low credit depending on the quality of
   your answer. A complete, fully correct answer receives the maximum 5 points. An answer that is
   partially incomplete or somewhat correct, but does not fully answer the question, receives 3
   points. A poor or incorrect answer receives 1 point. A complete lack of an answer receives no
   credit. The question diary is worth a total of 35 points (7 questions overall) with an additional
   10 points available (2 extra questions) for those students who wish to answer them.</p>
<p>To streamline index card collection at the end of class, you are responsible for placing your
   card in a collection box marked with your TA's name and class time. Any cards placed in the wrong
   box will not be graded, so be careful when you return your card. Be sure to include your name,
   your TA's name and your class days and time on your index card; any cards with incomplete or
   lacking information will not be graded.</p>

<h2>New York Times Project:</h2>
<p>A goal of this course is to demonstrate the relevance of biology to society today and to your own
   life. The New York Times project is designed with this in mind. You will receive an information
   sheet for subscribing to the New York Times at a greatly reduced rate negotiated by the Biology
   Department. Or, you may view the New York Times online via a link presented on Carmen. Read
   through the New York Times each week and choose an article addressing a topic in biology.
   Beginning the week of Oct. 6, turn into your TA each week for the next 7 weeks an article
   (original, photocopy or download) from the previous week's New York Times plus a written summary
   paragraph explaining:</p>
<ol style="list-style-type: lower-alpha">
 <li>the topic of the article,</li>
 <li>how it is important or relates to society,</li>
 <li>why you chose that article.</li>
</ol>
<p>This assignment is due to your TA at the beginning of recitation period. These written
   assignments are worth 7 points; the seventh assignment is worth 8 points for a total of 50
   points. The last assignment is due during the week of November 17th.</p>

