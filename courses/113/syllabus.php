<h2>Description</h2>
<p>Biology 113 is a course which, together with Biology 114, is designed to give the student a broad
   experience in the biological sciences. Science majors, including many health professionals, are
   the intended audience. The sequence will meet your General Education Curriculum requirement in
   the Natural Sciences. For those of you in the Colleges of the Arts and Sciences, Biology 113-114
   will meet the ten-hour sequence requirement. NOTE: IF YOU DO NOT HAVE A STRONG SCIENCE
   BACKGROUND, YOU BELONG IN ANOTHER COURSE, NAMELY, BIOLOGY 101-102.</p>

<h2>Prerequisites</h2>
<p>Chemistry 101 or 121 or consent of the instructor. If you have not had chemistry, you will need
   to fill out a form to determine whether the prerequisite can be waived.</p>

