<h2>Comments from the Faculty:</h2>
<p>For this course, you will be responsible for material presented in the lectures, and the
   corresponding reading assignments in the text. Anything mentioned in lecture may be on the exam
   even if it is not covered in the text. There may also be material in the text which is not
   covered in lecture. The lecture exams will not cover any material covered only in the laboratory
   portion of the course. That material will be tested separately in the lab.</p>
<p>In order to prepare for the exams, we recommend the following:</p>
<ul>
 <li>Read the text before coming to lecture. Become familiar with new terms.</li>
 <li>Take notes in lecture and pay particular attention to topics not discussed in the text. The
     lecture outlines will usually be available before a lecture on Carmen, and you should print
     them and bring them with you to class. The lecturers will assume that you have this material,
     and will usually be moving through slides at a speed that will not allow you to copy down every
     word, if you do not already have the notes.</li>
 <li>Write down questions about any topic that you find confusing, so that you can ask for
     clarification from your TA during recitation or ask the faculty lecturer for help during office
     hours.</li>
 <li>Be sure to keep up with the material. This course moves very rapidly and builds on previous
     material. Plan to spend at least 5&ndash;6 hours per week studying the lecture material. One
     hour spent early equals two or more hours just before the exam.</li>
 <li>Keep up with new terms (vocabulary) each week. Learning biology is like learning a new
     language. You will need to know much more than just terms, however, in order to get a good
     grade.</li>
 <li>The Study Guide for the text (available in bookstores) is useful, but not essential.</li>
 <li>Be sure that you reserve enough time for this course. If you work more than 20 hours per week,
     this should be the only major science course you are taking.</li>
</ul>

