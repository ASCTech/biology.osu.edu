<h1>Preferred Policy Statement</h1>

<h2>Objective:</h2>
<p>Students are expected to describe and defend a single potential policy option, of their
   preference, to their senator/congress-person. These papers will be no more than five pages in
   length plus a literature-cited page.</p>
<p>This assignment permits the student(s) to express their opinion as to which potential policy they
   prefer and why they feel it is best suited to solving the problems associated with the case
   study. Essentially, students are writing their senator's official policy. The argument for this
   policy is expected to be compelling and accurate. Students should thoroughly describe the reasons
   that make this the policy superior to the rest. Additionally, students must devote approximately
   one-page to the possible &quot;broad-sides&quot; their senator may expect from the opposition.
   These counter-arguments will likely come from the supporters of the alternative policies.
   Students are expected to thoroughly describe, and when possible refute, these allegations.</p>
<p>The paper should exhibit a logical flow of information. The opening paragraph should establish a
   clearly focused controlling idea, indicating the intended direction of the paper. Every paragraph
   should begin with a sentence that either suggests the topic of the paragraph or provides a smooth
   transition. Please note: The &quot;problem&quot; and the range of alternative policy
   &quot;options&quot;, as developed in your earlier 1 and 2-page papers, should be summarized in
   no more than about one paragraph each. Meanderings and unnecessary repetitions should be
   eliminated.</p>
<p>As you develop your argument, your sources of facts and supporting evidence must be properly
   referenced. Information you include from textbooks, newspapers, secondary journals (e.g.,
   Scientific American, National Geographic, The New Yorker, Atlantic Monthly, U.S. News &amp; World
   Report, etc.), lecture notes, and reputable websites must be referenced both in the text and
   with a complete citation included in the Literature Cited section of the paper. A minimum of
   five (5) references are required.</p>
<p>When quoting information verbatim, quotation marks must accompany the quotation. As a reminder,
   your audience is not a scientist, but still demands accurate and understandable information.
   Whenever possible, express information in your own words using appropriate referencing. This
   helps establish your knowledge and understanding of the background science.</p>

