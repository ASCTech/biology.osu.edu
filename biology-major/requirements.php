<p>The biology major provides a structured program that includes the
   major areas of importance to modern biology, as well as an in-depth
   concentration of study in one of the six departments in the College
   of Biological Sciences.</p>

<p>The requirements listed here are for individuals seeking a Bachelor
   of Science degree. A Bachelor of Arts degree is also available. That
   program follows the same requirements, except for Math 152, which is
   not required.</p>

<h2>Part A. Required Prerequisites or Supplements to the Major</h2>
<span class="detail">(Do not count toward the 45 hour major)</span>

<ul>
 <li>Biology 113 or H115, 114 or H116</li>
 <li>Mathematics 148, 150, 151, 152 or 161</li>
 <li>Chemistry 121 or H201, 122 or H202, 123 or H203</li>
 <li>Chemistry 251, 252, 254 or 245, 255 or 246</li>
 <li>Physics 111 or 131, 112 or 132, 113 or 133</li>
</ul>

