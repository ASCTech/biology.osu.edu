<h2>Part C. Specialization Areas</h2>

<p>In consultation with his or her advisor, each student must decide on an
   area of specialization within the broader field of biology. If the
   pre-approved specializations do not meet a student's needs, he or she may
   work with a biology advisor to choose a series of at least three advanced
   courses (300 and above, not including 591 and 597) in the biological
   sciences with a coherent theme, and present the proposed specialization to
   the College of Biological Sciences Curriculum Committee for approval.</p>

