<h2>Part C. Specialization Areas</h2>
<div class="detail">(Individually-designed areas of further study totaling at
   least 15 hours)</div>

<p>In consultation with his or her advisor, each student must decide on an
   area of specialization within the broader field of biology. If the
   pre-approved specializations do not meet a student's needs, he or she may
   work with a biology advisor to choose a series of at least three advanced
   courses (300 and above, not including 591 and 597) in the biological
   sciences with a coherent theme, and present the proposed specialization to
   the College of Biological Sciences Curriculum Committee for approval.</p>

<h3>Sample Specialization Areas for the Biology Major:</h3>
<ul>
  <li>Education in Life Sciences</li>
  <li>Forensic Biology</li> 
  <li>Pre-health Professions</li>
</ul>
<p>Please see a biology advisor for the lists of approved courses in these and
   other specializations.</p>

<h2>Part D. General Requirements for the Biology Major</h2>
<ul>
  <li>45 or more credit hours beyond the prerequisites to the major (some
      students may need additional courses at the 300-level or above after
      completing the Core and Specialization Area)</li>
  <li>Three courses in the major must have a laboratory or data analysis
      component.</li>
  <li>Independent Study, e.g. Biology 699 or H783, can be included to a
      maximum of 5 hours, and may be counted towards the laboratory/data
      analysis component.</li>
  <li>A minimum of C&ndash; in each course in the major</li>
  <li>An overall GPA of at least 2.0 in the major</li>
  <li>COURSES IN THE MAJOR MUST BE APPROVED BY A BIOLOGY ADVISOR.</li>
</ul>


