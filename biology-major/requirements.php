<h2>Part C. Electives within the Major</h2>

<ul>
 <li>At least three additional courses at the 300 level or above from one
     biological science department.</li>
 <li>At least two of the three courses must be lecture-based courses.</li>
 <li>When courses are taken as a sequence to satisfy a Core requirement (part
     B), all but one of the courses in the sequence may also be used to satisfy
     the three additional courses requirement.</li>
 <li>Independent study is strongly recommended (693 or H783), with up to five
     credit hours counting towards the 45 hour major.</li>
</ul>


<h2>Requirements &ndash; Parts B and C together</h2>

<ul>
 <li>A minimum grade of &quot;C&ndash;&quot; in each course and a 2.0 overall
     GPA in the major.</li>
 <li>At least three courses must have a laboratory.</li>
 <li>Total: 45 or more hours at the 300 level or above.</li>
 <li>COURSES IN THE MAJOR MUST BE APPROVED BY YOUR BIOLOGY ADVISER.</li>
</ul>


