<h2>Part D. General Requirements</h2>
<ul>
  <li>45 or more credit hours beyond the prerequisites to the major (some
      students may need additional courses at the 300-level or above after
      completing the Core and Specialization Area)</li>
  <li>Three courses in the major must have a laboratory or data analysis
      component.</li>
  <li>Independent Study, e.g. Biology 699 or H783, can be included to a
      maximum of 5 hours, and may be counted towards the laboratory/data
      analysis component.</li>
  <li>A minimum of C&ndash; in each course in the major</li>
  <li>An overall GPA of at least 2.0 in the major</li>
  <li>COURSES IN THE MAJOR MUST BE APPROVED BY A BIOLOGY ADVISOR.</li>
</ul>


