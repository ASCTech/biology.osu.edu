<p>The Introductory Biology Program (IBP) is an interdepartmental program
   administered by the <a href="http://www.biosci.ohio-state.edu/">College of
   Biological Sciences</a> at The Ohio State University. The Program's basic
   mission is to develop, teach, and provide administrative support for
   introductory-level Biology courses taught by the College of Biological
   Sciences. These courses include those that satisfy the General Education
   Curriculum (GEC) requirement for students who are not majoring in the
   biological sciences (Biology 101&ndash;102), for students who are planning on
   majoring in one of the biological sciences (Biology 113&ndash;114), an Honors
   sequence (Biology H115&ndash;H116), and other Biology courses that might be developed
   and taught.</p>
<p>The introductory level majors and non-majors courses taught by the IBP are
   an important component of the GEC of the Colleges of the Arts and Sciences.
   Within the paradigm of an arts and sciences curriculum, it is the mission of
   the IBP to provide courses that:
<ul>
 <li>meet the interests and needs of the diverse population of students served,</li>
 <li>are academically challenging and rewarding,</li>
 <li>are of the highest quality in both content and presentation.</li>
