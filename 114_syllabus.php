<h1>Biology 114</h1>
<h1>SAMPLE SYLLABUS</h1>

<h2>Description:</h2>
<p>Biology 114 is intended for students majoring in the natural sciences and is the continuation of
   Biology 113. In this course we deal mainly with the organismal and supra-organismal levels of
   biological organization. Evolution will be the unifying theme. The diversity, form, function, and
   ecology of organisms will be covered, with particular emphasis on plants and animals.</p>

<h2>Format:</h2>
<p>Lectures: 9:00 - 10:18 am, Tu & Th, Hitchcock 131</p>
<p>Laboratory/Recitation: 3 hr/week, 4180 Smith</p>
<p>Graduate TA's (GTA's) in charge of a laboratory/recitation section will usually be assisted by
   one or two undergraduate TA's (UTA's).</p>

<h2>Course Website:</h2>
<p>We plan to have most course information, including your grades, available on the web. You can
   access the course from Carmen at <a href="http://carmen.osu.edu/">carmen.osu.edu</a>. How to do
   so is described in &quot;Student Info for Carmen&quot; at the end of this syllabus. One feature
   of Carmen you might find useful is Bulletin Board. On the Bulletin Board you can post a question
   to me and/or to your classmates, and my reply (or Tracy's, or Mark's, or another student's) will
   be available to all. Obviously, you don't want to use this feature for purely personal questions.
   For these, refer above to &quot;What to do if you have a problem.&quot;</p>

<h2>Texts:</h2>
<p>Biology (1999, 6th edition), Campbell & Reece</p>
<p>Biology 114 Laboratory Manual, The Ohio State University</p>
<p>** Also useful are diagrams of the fetal pig, sheep brain, and calf heart available in the
   bookstores</p>

<h2>Text Coverage:</h2>
<p>Midterms will cover lecture material, not the Campbell text. Read Campbell for preparation,
   background, further information, review, MCAT's, etc., as you find helpful, but you will be
   tested only on those parts of the text I specifically tell you in lecture to read. (Campbell will
   also be valuable for several lab exercises.) Material from recitation and laboratory will not be
   the focus of questions on midterms, but I will expect you to be able to use information from
   these sources to answer questions. Occasional quizzes in recitation will cover material from
   recitation readings and laboratory exercises.</p>

<h2>Tests and Grades:</h2>
<p>The course will have two midterms, each worth 100 points, and a comprehensive final worth 150
   points. These tests will be multiple choice, fill-in, and short-answer. The laboratory will have
   a practical worth 80 points, unannounced quizzes worth 20 points, lab exercises worth about 150
   points, a New York Times clippings project worth 50 points, and readings (posted on the web) for
   discussion in recitation.</p>
