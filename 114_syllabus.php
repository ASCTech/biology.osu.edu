<h2>Make-up Exams:</h2>
<p>Make-ups must be completed within a week of the exam. A make-up may be taken only in cases in
   which absence from the exam is unavoidable, such as in cases of illness or family emergency (this
   must be documented). Please let me know if you are a graduating senior and need to take the final
   early.</p>

<h2>Change of Grades:</h2>
<p>You are responsible for making sure the grades we have are correct. You can verify your grades
   by checking them on Carmen. You have ten days after a grade is posted to request a change,
   including any requests for re-grading.</p>

<h2>Section Changes/Adds</h2>
<p>All section changes and adds must be done through the staff of the Center for Life Sciences
   Education, whose office is 4034 Smith Laboratory. Neither the lecturer nor other course personnel
   are authorized to make these changes.</p>

<h2>Recitation/Laboratory:</h2>
<p>The lab exercises are due at the beginning of recitation the following week (except the last,
   which is due in lab). The lab practical will cover the three anatomy labs. The (unannounced) lab
   quizzes will cover the preceding lab and anything you should have read before the current lab.</p>

<h2>Recitation Readings:</h2>
<p>There will be three reading assignments (available via Carmen), each worth about 12 points, due
   in recitation. Different TA's can choose different weeks to discuss the readings, so be sure to
   check with your TA. Each reading will have questions associated with it that you can download
   from Carmen. You should bring your answers to these questions with you to recitation. Part of
   recitation will be devoted to discussing the reading (including the pre-assigned questions, as
   well as others), after which you will turn in the answers you brought. Unlike other assignments,
   your answers cannot be turned in late!</p>

