<h1>Directions to Jennings Hall on the Ohio State campus</h1>
<h2 style="text-align:center">Please note: there is extensive construction in this area</h2>

<h2>From the south</h2>
<ul>
<li>Exit Rte 315 at Medical Center Exit</li>
<li>Turn the first <em><u>left</u></em> onto Cannon Drive</li>
<li>Turn <em><u>right</u></em> onto Herrick Drive (3<sup>rd</sup> light)</li>
<li>Turn <em><u>left</u></em> at the first intersection, onto Neil Drive; <strong>the Neil Avenue Parking Garage is on your left</strong></li>
<li>You should see students with signs by now!</li>
<li>Take the first left to enter the parking garage</li>
<li>The Jennings Hall Auditorium is to the south, across Herrick Drive, and the student escorts will stand ready to direct you</li>
</ul>

<h2>From the north</h2>
<ul>
<li>Exit Rte 315 at Lane Avenue</li>
<li>Turn <em><u>left</u></em> onto Lane Avenue</li>
<li>At the second intersection, turn <em><u>right</u></em> onto Olentangy River Road</li>
<li>Take a <em><u>left</u></em> at the first intersection, onto Herrick Drive</li>
<li>Proceed on Herrick Drive to the second intersection; <strong>the Neil avenue Parking Garage is on your left</strong></li>
<li>Turn left onto Neil Drive; you should see students with signs by now!</li>
<li>Take the first left to enter the parking garage</li>
<li>The Jennings Hall Auditorium is to the south, across Herrick Drive, and the student escorts will stand ready to direct you</li>
</ul>

