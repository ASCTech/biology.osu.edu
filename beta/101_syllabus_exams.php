<h2>Philosophy:</h2>
<p>This course is designed to teach the science of biology by doing biology. I believe the content of biology is best learned (as opposed to memorized)
   within the processes of biology. I will strive to demonstrate how major questions in biology have been addressed, and I will provide you with
   opportunities to participate in this process. At the end of this course I hope to have increased your fascination with biology and further developed
   your abilities to reason within this and other areas.</p>
<p>The course schedule should be considered tentative. I reserve the right to add or subtract time devoted to a given exercise in accordance with the
   overall goals of the course.</p>

<h2>Pedagogical (Teaching) Style:</h2>
<p>As indicated above, I want to show you how biologists have used objectivity and reasoning in trying to solve complex problems in biology. I also
   want you to learn some of the major concepts in biology: specifically evolution, genetics and energetics. I use a teaching approach commonly
   referred to as a learning cycle. This method entails several stages of thought for students: an initial phase of discovery, the formation of basic
   and tentative ideas (hypotheses) to explain what has been discovered, a test of those alternative explanations, an evaluation of the merits of each
   hypothesis, and an application of the insights learned. Virtually all major concepts in this course are first introduced in the laboratory exercise
   and then subsequently applied in lecture (see schedule of classes to see patterns of discovery and applications).</p>
<p>Using this approach, it is possible that past learning and studying techniques required in earlier biology courses, especially rote memorization,
   will not serve you well. I will try to give you study and understanding points along the way; these, however, will be of little use to you if you
   treat this as a traditional science course with your role comprised of learning terms and how to solve problems until the next exam. You have likely
   already had such courses. This course will use that information and those terms to understand the very basics of biology.</p>
<p>Some students will find this approach easier than middle or high school; others, more difficult. Understand that this course is designed to make you
   think and learn how biologists before us have learned to answer basic, yet sometimes complex questions in biology.</p>

