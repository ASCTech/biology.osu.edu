<h1>New York Times usage in Biology 101</h1>
<p>All students in Biology 101 are required to have a subscription, or regular access, to the New York Times. The New York Times is used in a variety
   of ways. All students do some form of New York Times project, which involves reading the Times each week to find science and biology related
   articles. A few professors are also using the Times for a segment of lecture called &quot;What's News&quot;, taking New York Times articles on
   current topics in biology each week, using them to facilitate discussion in class, and increase student awareness of biological issues.</p>
<p>Depending on instructor preference, the New York Times Project can take one of two forms&hellip;</p>
<u>Individual New York Times Clippings Project</u>:
<p>A major goal of this course is to demonstrate the relevance of biology to society today. Accordingly, students should turn in to their laboratory
   teaching assistants each week an article from the last week of the New York Times with a brief paragraph written explaining the article and how it
   indicates the relevancy of its biological topic to society today. These will be graded full credit or no credit and are due at the start of each lab
   period starting with the second lab exercise of the quarter. The article you submit should be clipped from the paper itself, photocopied or
   downloaded (the New York Times is available in most libraries and online at <a href="http://www.nytimes.com/">www.nytimes.com</a>).</p>
<u>Group New York Times Clippings Project</u>:
<p>Each cooperative lab group will assemble and submit a set of no less that six articles from the New York Times that are linked in such a way as to
   produce an integrated treatment of some issue in biology. The six articles and a type-written narrative of three pages discussing this issue will be
   due during lab in week 8 of the quarter. All members of the cooperative learning group will receive the same grade for the project.</p>
